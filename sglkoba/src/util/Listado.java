package util;

/**
 * @author  palito
 */
public class Listado {

	/**
	 * @uml.property  name="uno"
	 */
	private String uno;
	/**
	 * @uml.property  name="dos"
	 */
	private String dos;
	/**
	 * @uml.property  name="tres"
	 */
	private String tres;
	/**
	 * @uml.property  name="cuatro"
	 */
	private String cuatro;
	/**
	 * @uml.property  name="cinco"
	 */
	private String cinco;
	/**
	 * @uml.property  name="seis"
	 */
	private String seis;
	/**
	 * @uml.property  name="siete"
	 */
	private String siete;

	/**
	 * @return
	 * @uml.property  name="cinco"
	 */
	public String getCinco() {
		return cinco;
	}

	/**
	 * @return
	 * @uml.property  name="cuatro"
	 */
	public String getCuatro() {
		return cuatro;
	}

	/**
	 * @return
	 * @uml.property  name="dos"
	 */
	public String getDos() {
		return dos;
	}

	/**
	 * @return
	 * @uml.property  name="seis"
	 */
	public String getSeis() {
		return seis;
	}

	/**
	 * @return
	 * @uml.property  name="siete"
	 */
	public String getSiete() {
		return siete;
	}

	/**
	 * @return
	 * @uml.property  name="tres"
	 */
	public String getTres() {
		return tres;
	}

	/**
	 * @return
	 * @uml.property  name="uno"
	 */
	public String getUno() {
		return uno;
	}

	/**
	 * @param cinco
	 * @uml.property  name="cinco"
	 */
	public void setCinco(String cinco) {
		this.cinco = cinco;
	}

	/**
	 * @param cuatro
	 * @uml.property  name="cuatro"
	 */
	public void setCuatro(String cuatro) {
		this.cuatro = cuatro;
	}

	/**
	 * @param dos
	 * @uml.property  name="dos"
	 */
	public void setDos(String dos) {
		this.dos = dos;
	}

	/**
	 * @param seis
	 * @uml.property  name="seis"
	 */
	public void setSeis(String seis) {
		this.seis = seis;
	}

	/**
	 * @param siete
	 * @uml.property  name="siete"
	 */
	public void setSiete(String siete) {
		this.siete = siete;
	}

	/**
	 * @param tres
	 * @uml.property  name="tres"
	 */
	public void setTres(String tres) {
		this.tres = tres;
	}

	/**
	 * @param uno
	 * @uml.property  name="uno"
	 */
	public void setUno(String uno) {
		this.uno = uno;
	}

	public Listado(String uno, String dos, String tres, String cuatro, String cinco, String seis, String siete) {

		this.uno = uno;
		this.dos = dos;
		this.tres = tres;
		this.cuatro = cuatro;
		this.cinco = cinco;
		this.seis = seis;
		this.siete = siete;
	}
	
	
}
